package vux.codejava.response;

public class ExcellentIndividualResponse {

	private Integer id;
	private Long userId;
	private String fullName;
	private String content;
	private String imageAvatar;
	private String imageAvatarResize;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImageAvatar() {
		return imageAvatar;
	}
	public void setImageAvatar(String imageAvatar) {
		this.imageAvatar = imageAvatar;
	}
	public String getImageAvatarResize() {
		return imageAvatarResize;
	}
	public void setImageAvatarResize(String imageAvatarResize) {
		this.imageAvatarResize = imageAvatarResize;
	}
	
	
}
